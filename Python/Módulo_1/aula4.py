# Tipos: int e float
# int -> Número Inteiro
# ABAIXO - ZERO - ACIMA DE ZERO
print(11) #int
print(-11) #int
print(0) #int

# float -> Número com Ponto Flutuante (casas decimais)
print(1.1, 10.11)
print(0.0, -1.5)

# A função type mostra o tipo que o Python inferiu ao valor
print(type('Vinicius'))
print(type(1))
print(type(1.0))
