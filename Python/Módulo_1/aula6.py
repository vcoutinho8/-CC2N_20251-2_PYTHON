# Conversão de tipos, coerção
# type convertion, typecasting, coercion
# é o ato de converter um tipo em outro
# tipos imutáveis e primitivos:
# str, int, float, bool
print(int('1'), type(int('1'))) # DE UMA STR PARA TIPO INT
print(float('1') + 1) # DE UMA STR PARA TIPO FLOAT
print(bool(' ')) 
print(str(11) + 'b') # DE UM INT PARA TIPO STR